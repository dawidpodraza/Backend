public class Calculate {
    private int[] tabA;
    private int[] tabB;

    public Calculate(int[] tabA, int[] tabB) {
        this.tabA = tabA;
        this.tabB = tabB;
    }

    public int[] getTabA() {
        return tabA;
    }

    public void setTabA(int[] tabA) {
        this.tabA = tabA;
    }

    public int[] getTabB() {
        return tabB;
    }

    public void setTabB(int[] tabB) {
        this.tabB = tabB;
    }
}
